# zad70

# Xi = 1 ако се пада ези при i-тото хвърляне, 0 в противен случай
# p = P(Xi=1)
# X=X1+..+Xn, n=100     // X~Bi(n, p)
# H0: p = 1/2, H1: p > 1/2    // хипотези, пита се коя изглежда
#                             // по-правдоподобна
# X/n -> p      // за големи n
# грешка от I тип = {отхвърляме H0, ако H0 е вярна}
# искаме P(грешка от I тип)=alpha, alpha=0.05 // ппц 0.05, 0.01, 0.1
#                                             // т.е. малко число
#           X/n-p
# Z =  ---------------  ~ N(0, 1) от ЦГТ за големи n 
#      sqrt(p(1-p)/n)

# z-obs = горната ф-ла но с малко x // това ще е конкретната ст-т
# x = 58, n = 100, p = 1/2          // знаем n и x, p е от хипотезата

# дефинираме p.value = P(Z >= z-obs) // 1-pnorm(z-obs)
# ако p.value <= alpha=0.05 => отхвърляме H0 в полза на H1
# ако p.value >= alpha  => не можем да отхвърлим H0
x <- 58
p <- 1/2
n <- 100
z.obs <- (x/n - p) / (sqrt(p*(1-p)/n))
z.obs

p.value <- 1-pnorm(z.obs)
p.value # ~= 0.054
# не можем да отхвърлим нулевата хипотеза H0
# т.е. не можем да заключим, че вероятността да се падне ези при
# хвърляне на тази монета е повече от 1/2. // H1


