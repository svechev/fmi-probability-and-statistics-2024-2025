# zad72
# Xi = диаметър на i-тата случ. избрана топка
# n = 45,   1 <= i <= 45
# miu = E(Xi),  sigma = sqrt(D(X)) = 0.12  // съмнения за miu = 6.7
# H0: miu = 6.7    H1: miu != 6.7
# X черта = 1/n(X1+..+Xn)   X черта -> miu

# Z = (Xчерта - miu) / sigma/sqrt(n)   ~ N(0, 1)
# z-obs = същото с х черта малко  // наблюдаваната стойност 6.73
# щом хипотезата е != (а не <) тогава p.value ще бъде
# P(Z <= -|z-obs|) + P(Z >= |z-obs|)
# 2*P(Z<=-|z-obs|)     // понеже е симетрично

n <- 45
sigma <- 0.12
x.bar <- 6.73    # х черта
mu <- 6.7   # от нулевата хипотеза

z.obs <- (x.bar - mu) / (sigma/sqrt(n))
z.obs

p.value <- 2*pnorm(abs(z.obs)*(-1))
p.value #  ~= 0.09 > 0.05
# не можем да отхвърлим нулевата хипотеза
# данните подкрепят твърдението, че средният диаметър на топките,
# произведени от машината е 6.7 см.


