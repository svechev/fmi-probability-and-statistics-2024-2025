# zad74
# Xi = концентрация на кадмий в i-тата гъба
# 1 <= i <= n,  n = 10
# miu = E(Xi)
# H0: miu = 4      H1: miu < 4
# X черта = 1/n(X1+..+Xn)
# S = sqrt( 1/(n-1)  *  сума по i от (Xi-X)^2 )

# T = (Xчерта-miu) / (S/sqrt(n)) ~ t-разпределение с (n-1)
#                                  степени на свобода
# t-obs = същото с x малко // конкретните ст-ти
# p.value = P(T <= t-obs)   // хипотеза за по-малко
# смята се с pt(t-obs, df=n-1)

x <- c(3.1, 3.0, 3.7, 2.6, 4.2, 3.8, 3.6, 2.7, 3.8, 4.4)
x.bar <- mean(x)
s <- sd(x)     # смята онази сума с корен за S (от ред 97)
n <- length(x)
mu <- 4  # хипотеза

t.obs <- (x.bar - mu) / (s/sqrt(n))
t.obs

p.value <- pt(t.obs, df=n-1)
p.value # ~= 0.013 < 0.05
# отхвърляме нулевата хипотеза
# Имаме основание да твърдим, че средната концентрация на
# кадмий в горските гъбите в дадената местност е по-малко от 4 мг/кг.

# help(t.test)   друг начин: 
t.test(x, mu=4, alternative="less")

